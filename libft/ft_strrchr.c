#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*last;

	if (s == NULL)
		return (NULL);
	last = NULL;
	while (*s)
	{
		if (*s == (char)c)
			last = (char *)s;
		s++;
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (last);
}
